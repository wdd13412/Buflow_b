    fluxresidualsb(1:nc, 1:nv) = RESHAPE(psi(1:n), [nc, nv])
    data_4d137b_adj = 0.0_8
    cellprimitivesoutb_adj = 0.0_8
    CALL COMPUTE_STEADY_RESIDUAL_B(data_4d137_adj, data_4d137b_adj, cellprimitivesout_adj, cellprimitivesoutb_adj)
    dJdp(1:4) = dJdp_partial(1:4) - data_4d137b_adj(1, 1:4)
  END SUBROUTINE COMPUTE_DJDP

